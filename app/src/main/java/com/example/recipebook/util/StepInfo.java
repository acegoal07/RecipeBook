package com.example.recipebook.util;

public class StepInfo {
    String step;

    /**
     * Constructor for StepInfo
     * @param step The step
     */
    public StepInfo(String step) {
        this.step = step;
    }

    /**
     * Gets the step
     * @return The step
     */
    public String getStep() {
        return this.step;
    }
}
